;=====================DATA FILE DOCUMENTATION====================================
;
;This data file contains the shape tables for the tile graphics used in 
;the game, or at least on the surface map (we might have differnet shape tables for other maps like towns, dungeons, etc)
;
;The static tile section must start at exactly $7000 and end at exactly $7FFF.
;The animated tile section must start at exactly $8000 and end at exactly $BFFF.
;This prevents the tile numbers from getting out of sequence, since the tile number is
;used as a memory address offset. 
;
;(HISTORICAL NOTE ONLY) Using DOS RWTS, before the ProDOS IO driver was integrated, 
;This was also because this is the memory reserved for static tiles and it
;is factored into the track and sector's at which the boot loader expects
;to find this data. i.e. if the size of the LOADER.BIN file changes because
;this code section is shortened, that will alter the track/sector location of
;the file on disk, and the bootloader will load track/sectors which do
;not contain the correct data. 
;====end historical note
;
;
;Ensuring the same code size is managed a little differently
;for static and animated tiles. In the static tile section, there are
;blank tiles (with dummary data) inserted in between tiles with real 
;data. When a new tile is added, a blank tile must be removed. 
;
;In the animated tile section, a blank only needs to be added if a Tile_ID
;is skipped. The size of the animated tile section is trued up by a 
;.NO directive at the start of the WORLD ZONE DATA section. In theory
;this coud have been done in the static tile section (i.e put a .NO
;directive at the start of the animated tile section), but so many
;static tile IDs at various parts of the range were used early on I 
;thought it easier just to create all the necessary blank tiles at
;one time. 
;=================================================================================


