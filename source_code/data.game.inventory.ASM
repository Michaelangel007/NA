;=====.ASM FILE DOCUMENTATION====================================
;
;This file contains contains the following data sections:
;		-player's readied equipment (working copy)
;		-player's inventory data (working copy)
;		-player's readied equipment (saved game copy)
;		-player's inventory data (saved game copy)
;		-game item tables (weapon, armor, misc)
;
;==FILE I/O==
;The file is setup to be opened once and then for records in any of the 
;tables to be read via a file_offset_reset/seek/read routine. The 
;file_offset_reset (/aka seek reset) allows the seek to start from the beginning
;of the file, which greatly simplies the calculation of the number of seek bytes.
;
;The seek calculations are done using the appropriate ";ITEM FILE OFFSETS" constants at the end
;of the file (seeks to the start of the desired table) + the offset to the specific record 
;of the table to read.
;
;The reason the file offset reset method is used instead of opening the file every time is because opening
;files is very slow due on floppy due to ProRWTS needing to search for the location of the file on the floppy disk. 
;
;==SAVED GAME DATA== (not yet implimented)
;The plan is that when the player saves the game the working copy of the inventory and readied equipment data
;will be copied to the saved game copy. On game launch, the saved game copy
;will be read in and then written to the working copy. 
;
;=================================================================================

	;no filler byte needed because all file I/O routines use the file offset reset (/aka seek reset) method.  

	
