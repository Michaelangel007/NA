;-Production Release Setup (speculation)
;
;I think that the build process should generate a target binary
;file for this .ASM file (as it does now), and write it to a disk image (as it does now)
;Instead of hard coded PC data, this file should contain init values (probably $00 or $FF). 
;
;Essentially, the build process should create disk images that contain a brand new copy of the game
;ready to be played for the first time. Thus, the PC character data file exists because all files ProRWTS will write to need
;to exist at build-time, but it will contain init values because the player will go through character creation at 
;game boot. Detecting this file has init values might be the way that the game launcher determines
;that new character creation is needed to proceed to the game. 
;
;=================================================================================


