;=====================DATA FILE DOCUMENTATION====================================
;
;
;
;This data file contains the shape tables for the tile graphics used in 
;"building" locations (i.e. towns, castles, etc.). 
;
;As with all data files, it must remain a static size or the track/sector location
;on disk will change and my.rwts will load the wrong data. 
;
;In general, the .NO statement at the end maintains the static size,
;and as long as a blank is added for any tile removed and vice-versa in the 
;static tile section, everything will be fine.
;
;See the documentation in data.shapes.surface.asm for a more detailed 
;explanation. 
;
;
;-Deeper Technical Notes
;
;The filler data at the start of the tile is required. AppleCommander adds
;a 4 byte header when writing a file to a disk image. The 
;filler data is equal to !252 bytes. The result is that the 
;real data starts at exactly at the start of the 2nd sector. 
;The .ORG is set to $1F04 I(instead of $2000) so that the address 
;scheme in the .lst file will have the page boundaries match the sector 
;boundaries. 
;
;This may seem to waste !252 bytes of disk, and it does, but for 
;data written to disk which is in equal page legnths, there will be
;!252 bytes wasted either at the front end or back end. i.e. even if the filler
;data wasn't included and the program code stripped off the 4 byte header,
;that wouldn't save any disk becasue the last 4 bytes of real data would end up pushed into
;a sector by themselves. 
;
;Also note, since this is a data files the .ORG statement 
;doesn't do anything except control the number scheme in the
;.LST file and become a basis for the .NO statement determining
;how much filler data to add. Due to the effect on the .NO
;statement it is possible (likely) to break the graphcis
;on screen by modifying the .ORG statement. In summary, the address chosen for .ORG was arbitary when it
;was set but now other things depend on it. 
;
;WARNING: for future modification, be very careful about
;reading in more than one track/sector at a time. As the
;code is written that would result in overflowing the
;array at the destination address and clobbering other 
;variables in memory (notably the zone hi/lo address table),
;which comes immediately after.
;
;
;=================================================================================
				
