	CMP #$00
	BNE .WEST.NEXT_TILE
	
	JSR DRAW.TILE.SINGLE
	
.WEST.NEXT_TILE
	CPY #SCREEN.ARRAY.LAST_ELEMENT
	BEQ .EXIT
	
	TYA
	CLC
	ADC #SCREEN.ARRAY.OFFSET
	TAY
	
	JMP .WEST.LOOP	
