	LDX #$00							;SCREEN ARRAY INDEX
	
.LOOP
	LDA SCREEN.DARK.DATA,X				;LOAD SCREEN POSITION OF CURRENT TILE BEING EVALUATED
	CMP #$01							;IS CURRENT TILE FLAGGED DARK?
	BNE .NEXTTILE						;IF NO, NEXT TILE

