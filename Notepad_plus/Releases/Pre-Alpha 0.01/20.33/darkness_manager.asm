	STA SCREEN.DARK.ALGORITHM.LOCATION_CURRENT
	
	DEC SCREEN.DARK.ALGORITHM.LOCATION_STOP		;INCREMENT STOP COLUN (KEEP ROW THE SAME, WE'RE AT THE BOTTOM ROW ALREADY)
